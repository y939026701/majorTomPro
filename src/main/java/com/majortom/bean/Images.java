package com.majortom.bean;


public class Images {

  private long cIId;
  private long bindId;
  private long type;
  private String imgUrl;


  public long getCIId() {
    return cIId;
  }

  public void setCIId(long cIId) {
    this.cIId = cIId;
  }


  public long getBindId() {
    return bindId;
  }

  public void setBindId(long bindId) {
    this.bindId = bindId;
  }


  public long getType() {
    return type;
  }

  public void setType(long type) {
    this.type = type;
  }


  public String getImgUrl() {
    return imgUrl;
  }

  public void setImgUrl(String imgUrl) {
    this.imgUrl = imgUrl;
  }

}
